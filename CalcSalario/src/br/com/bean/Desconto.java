package br.com.bean;

public class Desconto {

	private Integer id_funcionario;
	private Integer id_desconto;
	private double vl_desconto;
	public Integer getId_funcionario() {
		return id_funcionario;
	}
	public void setId_funcionario(Integer id_funcionario) {
		this.id_funcionario = id_funcionario;
	}
	public Integer getId_desconto() {
		return id_desconto;
	}
	public void setId_desconto(Integer id_desconto) {
		this.id_desconto = id_desconto;
	}
	public double getVl_desconto() {
		return vl_desconto;
	}
	public void setVl_desconto(double vl_desconto) {
		this.vl_desconto = vl_desconto;
	}
	
	
}
